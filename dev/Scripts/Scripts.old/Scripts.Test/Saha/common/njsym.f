      SUBROUTINE GENJ45(IP)
*
*      FIND THE LEVEL OF EACH J IN THE COUPLING TREES OF J2 AND J3 AND
*      STORE IN THE J4 AND J5 ARRAYS RESPECTIVELY. IF AN ELEMENT OF J1
*      DOES NOT OCCUR IN J2 THE J4 ENTRY IS -1 AND IF AN ELEMENT DOES
*      NOT OCCUR IN J3 THE J5 ENTRY IS -1
*
      COMMON/COUPLE/M,N,J1(60),J2(18,3),J3(18,3)
      COMMON/DEPTHS/J4(60),J5(60)
*
*
*
      DO 1 I=1,M
      DO 2 I2=1,IP
*
*      STORE LEVEL OF EACH J IN J2 ARRAY IN J4
*
      IF (J2(I2,1)-I) 3,4,3
 3    IF (J2(I2,2)-I) 2,4,2
 2    CONTINUE
      DO 17 I2 = 1,IP
      IF (J2(I2,3)-I) 17,18,17
 17   CONTINUE
      J4(I) = -1
      GO TO 5
 18   J4(I) = 0
      GO TO 5
 4    I3 = 1
 9    DO 6 I4 = 1,IP
      IF (J2(I4,1)-J2(I2,3)) 7,8,7
 7    IF (J2(I4,2)-J2(I2,3)) 6,8,6
 6    CONTINUE
      J4(I) = I3
      GO TO 5
 8    I3 = I3+1
      I2 = I4
      GO TO 9
*
*      STORE LEVEL OF EACH J IN J3 ARRAY IN J5
*
 5    DO 10 I2 = 1,IP
      IF (J3(I2,1)-I) 11,12,11
 11   IF (J3(I2,2)-I) 10,12,10
 10   CONTINUE
      DO 19 I2 = 1,IP
      IF (J3(I2,3)-I) 19,20,19
 19   CONTINUE
      J5(I) = -1
      GO TO 1
 20   J5(I) = 0
      GO TO 1

 12   I3 = 1
 16   DO 13 I4 = 1,IP
      IF (J3(I4,1)-J3(I2,3)) 14,15,14
14    IF (J3(I4,2)-J3(I2,3)) 13,15,13
 13   CONTINUE
      J5(I) = I3
      GO TO 1
 15   I3 = I3+1
      I2 = I4
      GO TO 16
 1    CONTINUE
      RETURN
      END
