      RECUP = RECUP*X1
      GO TO 1
*
*      JWRD IS THE NUMBER OF RACAH COEFFICIENTS WHICH INVOLVE A
*      SUMMATION
*      JWORD(I,J),I=1,6,J=1,JWRD CONTAINS THE NUMBER WHICH GIVE THE
*      LOCATION OF THE J VALUES FOR THE RACAH COEFFICIENTS EITHER IN THE
*      J1 LIST OR IN THE JSUM1 LIST
*
    3 JWRD = JWRD+1
      DO 5 J=1,6
      JWORD(J,JWRD)=JW(J,I)
*
*      MAXJWE CONTAINS THE MAXIMUM J IN THE LIST OF VARIABLES TO BE
*      SUMMED OVER
*
      IF(MAXJWE-JW(J,I))  215,5,5
  215 MAXJWE=JW(J,I)
    5 CONTINUE
    1 CONTINUE
  302 J6CP=0
      IF(J6C)300,300,301
*
*      J6P(I),I=1,J6CP CONTAINS ALL J6 WHICH INVOLVE  A SUMMATION
*      MULTIPLY RECUP BY ALL THOSE WHICH DO NOT
*
  301 DO 6 I=1,J6C
      IF(J6(I)-M) 7,7,21
    7 I1=J6(I)
      RECUP = RECUP*DSQRT(DFLOAT(J1(I1)))
      GO TO 6
   21 J6CP = J6CP+1
      J6P(J6CP)=J6(I)
    6 CONTINUE
  300 IASTOR = 0
      J7CP = 0
      IF(J7C) 303,303,304
*
*      J7P(I),I=1,J7CP CONTAINS ALL J7 WHICH INVOLVE  A SUMMATION.
*      MULTIPLY RECUP BY ALL THOSE WHICH DO NOT
*
  304 DO 8 I=1,J7C
      IF(J7(I)-M) 9,9,22
    9 I1=J7(I)
      IASTOR = IASTOR + J1(I1) -1
      GO TO 8
   22 J7CP = J7CP+1
      J7P(J7CP)=J7(I)
    8 CONTINUE
  303 J8CP=0
      IF(J8C) 305,305,306
*
*      J8CP(I),I=1,J8CP CONTAINS ALL J8 WHICH INVOLVE A SUMMATION
