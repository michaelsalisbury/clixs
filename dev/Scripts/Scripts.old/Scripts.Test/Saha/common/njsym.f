   29 CONTINUE
      WRITE(IWRITE,35)
      STOP
   30 J6P(I)=J
   28 CONTINUE
   26 IF(J7CP) 130,130,31
*
*      CHECK THAT NO EXTRA SUMMATION VARIABLES OCCUR IN J7P, SET J7P
