      SUBROUTINE GENI9(IP)
*
*      DETERMINES THE NUMBER OF RECOUPLING NECESSARY TO BRING J2(I5,I3)
*      AND J2(I6,I4) INTO THE SAME TRIAD. THIS WILL GIVE A TRIAD
*      IDENTICAL WITH ONE IN J3. ON EXIT I9 CONTAINS THE NUMBER OF
*      RECOUPLINGS PLUS TWO,I7 CONTAINS THE LEVEL OF THE I5 TRIAD BELOW
*      THE COMMON TRIAD AND I8 CONTAINS THE LEVEL OF THE I6 TRIAD BELOW
*      THE COMMON TRIADS
*      SEE DESCRIPTION OF COMMON BLOCK WCOMI9 FOR FURTHER DETAILS
*
      COMMON/COUPLE/M,N,J1(60),J2(18,3),J3(18,3)
      COMMON/DEPTHS/J4(60),J5(60)
      COMMON/WCOMI9/I3,I4,I5,I6,I7,I8,I9,I17,I18,I19,I20
*
*
*
      I1 = J4(I7)
      I2 = J4(I8)
*
*      DETERMINES WHICH J OF J2(I5,I3) AND J2(I6,I4) LIES LOWEST, STORE
*      LOWEST AS J2(I18,I20) AND HIGHEST AS J2(I17,I19)
*
      IF (I1-I2) 1,1,3
 1    I17 = I5
      I18 = I6
      I19 = I3
      I20 = I4
      I3 = I2-I1
      I7 = 0
      I8 = I3
      I4 = I1
      IF (I3) 8,8,2
*
*      I6 DENOTES THE LOWEST TRIAD,SCAN TRIADS TO FIND NEW TRIAD I6 AT
*      SAME LEVEL AS I5
*
 2    DO 4 I = 1,I3
      DO 5 J = 1,IP
      IF (J2(J,1)-J2(I6,3)) 7,6,7
 7    IF (J2(J,2)-J2(I6,3)) 5,6,5
 5    CONTINUE
      J=IP
 6    I6 = J
 4    CONTINUE
      GO TO 8
 3    I17 = I6
      I18 = I5
      I19 = I4
      I20 = I3
      I3 = I1-I2
      I7 = I3
      I8 = 0
*
*      I5 DENOTES THE LOWEST TRIADS. SCAN TRIADS TO FIND NEW TRIAD I6 AT
*      SAME LEVEL I5
*
      DO 9 I = 1,I3
      DO 12 J = 1,IP
      IF (J2(J,1)-J2(I5,3)) 10,11,10
 10   IF (J2(J,2)-J2(I5,3)) 12,11,12
 12   CONTINUE
      J=IP
 11   I5 = J
 9    CONTINUE
      I4 = I2
*
*      I5 AND I6 NOW DENOTES TRIADS AT SAME LEVEL. I4 CONTAINS THE
*      COMMON LEVEL
*
 8    DO 13 I = 1,I4
      I1 = I
      IF (I5-I6) 14,21,14
*
*      I5 AND I6 DENOTE DIFFERENT TRIADS SCAN TO FIND TRIADS AT NEXT
*      LEVEL WHICH REPLACE I5 AND I6
*
 14   DO 15 J = 1,IP
      IF (J2(J,1)-J2(I5,3)) 16,17,16
 16   IF (J2(J,2)-J2(I5,3)) 15,17,15
 15   CONTINUE
      J=IP
17    I5 = J
      DO 18 J = 1,IP
      IF (J2(J,1)-J2(I6,3)) 19,20,19
 19   IF (J2(J,2)-J2(I6,3)) 18,20,18
 18   CONTINUE
      J=IP
 20   I6 = J
 13   CONTINUE
*
*      I5 AND I6 NOW BOTH DENOTE THE COMMON TRIAD
*
 21   I9 = I3+2*I1
      I8 = I8+I1
      I7 = I7+I1
      RETURN
      END
