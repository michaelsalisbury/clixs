      JWORD(J,I)=M+I1
   15 CONTINUE
   14 CONTINUE
      IF(KFL6.LT.NSUM) THEN
         WRITE(IWRITE,308)
         STOP
      END IF
  307 IF(J6CP) 26,26,27
