      DO 15 J=1,6
      IF(JWORD(J,I)-M) 15,15,16
   16 NSUM=NSUM+1
      IF(NSUM-1) 17,17,18
*
*      HAS THE SUMMATION VARIABLE OCCURED BEFORE. IF NOT INCLUDE IN
*      JSUM1 LIST
*
   18 NSUM1 = NSUM-1
      DO 19 I1=1,NSUM1
      IF(JWORD(J,I)-JSUM1(I1)) 19,20,19
   19 CONTINUE
   17 JSUM1(NSUM)=JWORD(J,I)
      I1=NSUM
      GO TO 25
   20 NSUM =NSUM1
*
*      JSUM6(I),I=1,NSUM IS THE NUMBER OF TIMES EACH SUMMATION VARIABLE
*      OCCURS IN JWORD
*
   25 JSUM6(I1)=JSUM6(I1)+1
      I2=JSUM6(I1)
