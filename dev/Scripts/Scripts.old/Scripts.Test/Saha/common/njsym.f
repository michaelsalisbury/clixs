*
   13 NSUM=0
      MAXSUM=MAXJWE-M
      DO 24 I=1,MAXSUM
      JSUM6(I)=0
      JSUM7(I)=0
   24 CONTINUE
*
*      FIND SUMMATION VARIABLES
*
