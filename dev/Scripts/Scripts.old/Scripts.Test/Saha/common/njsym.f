   36 FORMAT (21H FAIL IN GENSUM AT 36)
   42 FORMAT (21H FAIL IN GENSUM AT 42)
   63 FORMAT (21H FAIL IN GENSUM AT 63)
  169 FORMAT(22H 169...        RECUP =,F12.8,7H STOR =,F12.8,8H STOR1 =,
     :F12.8)
  170 FORMAT(18H 170...       IST ,6I4)
  190 FORMAT(8H WSTOR =,10F10.6)
  308 FORMAT(23H KFL6 DIMENSION FAILURE)
  311 FORMAT(22H FAIL IN GENSUM AT 310)
*
*
*
*
*
*      S E C T I O N  1
*
*      EVALUATES ALL TERMS IN J6,J7,J8 AND JW WHICH DO NOT INVOLVE A
*      SUMMATION AND FORM MODIFIED ARRAYS J6P,J7P,J8P AND JWORD WHICH DO
*      THE RESULT OF THE EVALUATION IS STORED IN RECUP AND AISTOR
*
      RECUP=1.D0
      MAXJWE=M
      JWRD = 0
      IF(JWC)302,302,185
*
*      MULTIPLY RECUP BY ALL RACAH COEFFICIENTS WHICH DO NOT INVOLVE A
*      SUMMATION
*
