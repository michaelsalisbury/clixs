     :JSUM2(18),JSUM4(18,20),JSUM5(18,20),JSUM3(18),JSUM6(18),
     :JSUM7(18),JSUM8(18),JSUM(2,20),JWTEST(20),WSTOR(20),IPAIR(2,2)
      DIMENSION J6(60),J7(80),J8(60),JW(6,20)
      COMMON/COUPLE/M,N,J1(60),J2(18,3),J3(18,3)
      COMMON/DEBUG/IBUG1,IBUG2,IBUG3,NBUG6,NBUG7,IFULL
      COMMON/DIMEN/KFL1,KFL2,KFL3,KFL4,KFL5,KFL6,KFL7,MXIHSH
      COMMON/INFORM/IREAD,IWRITE,IOUT,ISC1,ISC2,ISC3,JSC0,JSC1,JSC2,JSC3
*
*      FORMAT STATEMENTS USED IN GENSUM
*
