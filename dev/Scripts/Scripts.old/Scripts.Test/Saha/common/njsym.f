   23 J8CP=J8CP+1
      J8P(J8CP)=J8(I)
   10 CONTINUE
*
*      NO RACAH COEFFICIENTS REMAINING AND THUS NO SUMMATIONS TO BE
*      CARRIED OUT IF JWRD=0. JUMP TO END TO INCLUDE (-1) FACTORS IN
*      RECUP AND THEN EXIT
*
  305 IF(JWRD) 12,12,13
*
*
*
*
*
*      S E C T I O N  2
*
*      SEARCH THROUGH THE JWORD LIST TO FIND ALL THE SUMMATION VARIABLES
