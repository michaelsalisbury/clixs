*      CHECK THAT NO EXTRA SUMMATION VARIABLES OCCUR IN J6P. SET J6P
*      EQUAL TO THE LOCATION IN JSUM1 LIST OF SUMMATION VARIABLE
*
   27 DO 28 I=1,J6CP
