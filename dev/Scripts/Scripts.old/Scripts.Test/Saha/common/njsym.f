*
*
*
*
*
*      S E C T I O N  3
*
*      ORDERS THE SUMMATION VARIABLES SO THAT THE RANGE OF EACH
*      SUMMATION HAS BEEN PREVIOUSLY DEFINED
*
   37 NCT  =0
      NCT1 = 0
   64 DO 43 I=1,JWRD
      DO 44 J=1,6
      I1=JWORD(J,I)-M
      IF(I1) 44,44,45
